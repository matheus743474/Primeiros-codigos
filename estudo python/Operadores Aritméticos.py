n1 = float(input("Digite um número: "))
n2 = float(input("Digite outro número: "))

s = n1 + n2
sb = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2

print(f"A soma é {s:.2f}\nA subtração é {sb:.2f}\nA multiplicação é {m:.2f}\nA divisão é {d:.2f}\nA divisão inteira é {di:.2f}\nA exponenciação é {e:.2f}")